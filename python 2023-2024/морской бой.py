def show_table(table):
    print("""   1 2 3 4 5 6 7 8 
    _______________""")
    for i in range(len(table)):
        print(f"{i+1} |",end='')
        print(*table[i],end='|\n')
    print("   ---------------")

def put_ship(table, x, y, ship_size):
    x,y=x-1, y-1
    
    if ship_size!=1:
        print("–ö–æ—Ä–∞–±–ª—å –±—É–¥–µ—Ç —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ (1) –∏–ª–∏ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ(2)?")
        sync=int(input())
        if sync==1:            
            for i in range(y,y+ship_size):
                table[i][x]="#"
        else:
            for i in range(x, x+ship_size):
                table[y][i]="#"
    else:
        table[y][x]='#'

